package pojoclass;

public class Tags {
	
	int id;
	String name;
	public Tags(int id, String name) {
		super();
		this.id = id;
		this.name = name;
	}
	
	
	public Tags()
	{
		
	}


	public int getId() {
		return id;
	}


	public String getName() {
		return name;
	}


	public void setId(int id) {
		this.id = id;
	}


	public void setName(String name) {
		this.name = name;
	}
	
	

}
